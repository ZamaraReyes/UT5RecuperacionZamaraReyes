package ut5recuperacionzamarareyes;

/**
 *
 * @author ZareRed
 */
public class Calculadora {
    
    private String Sumar;
    private String Restar;
    private String Multiplicar;
    private String Dividir;

    /**
     * @return the Sumar
     */
    public String getSumar() {
        return Sumar;
    }

    /**
     * @param Sumar the Sumar to set
     */
    public void setSumar(String Sumar) {
        this.Sumar = Sumar;
    }

    /**
     * @return the Restar
     */
    public String getRestar() {
        return Restar;
    }

    /**
     * @param Restar the Restar to set
     */
    public void setRestar(String Restar) {
        this.Restar = Restar;
    }

    /**
     * @return the Multiplicar
     */
    public String getMultiplicar() {
        return Multiplicar;
    }

    /**
     * @param Multiplicar the Multiplicar to set
     */
    public void setMultiplicar(String Multiplicar) {
        this.Multiplicar = Multiplicar;
    }

    /**
     * @return the Dividir
     */
    public String getDividir() {
        return Dividir;
    }

    /**
     * @param Dividir the Dividir to set
     */
    public void setDividir(String Dividir) {
        this.Dividir = Dividir;
    }
    
}
